/**
 * Obter ícone como string SVG
 * @param {string} name - Nome do ícone
 * @returns {string} SVG string
 */
export function getIcon(name) {
  return icons[name] || icons.info;
}

/**
 * Criar elemento SVG
 * @param {string} name - Nome do ícone
 * @param {object} options - Opções (className, size, color, etc)
 * @returns {HTMLElement} Elemento SVG
 */
export function createIconElement(name, options = {}) {
  const svg = document.createElement('span');
  svg.innerHTML = getIcon(name);
  
  if (options.className) {
    svg.className = options.className;
  }
  
  if (options.size) {
    const elem = svg.querySelector('svg');
    elem.setAttribute('width', options.size);
    elem.setAttribute('height', options.size);
  }
  
  if (options.color) {
    const elem = svg.querySelector('svg');
    elem.style.color = options.color;
  }
  
  if (options.ariaLabel) {
    svg.setAttribute('aria-label', options.ariaLabel);
    svg.setAttribute('role', 'img');
  }
  
  return svg;
}

/**
 * Substituir ícone emoji por SVG
 * @param {HTMLElement} element - Elemento contendo emoji
 * @param {string} iconName - Nome do ícone SVG
 */
export function replaceEmojiWithIcon(element, iconName) {
  const svgElement = createIconElement(iconName);
  element.replaceWith(svgElement);
}

